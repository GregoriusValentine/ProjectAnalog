    private void tbSIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbSIMPANActionPerformed

        //FUNGSI KETIKA TEXT FIELD KOSONG MAKA AKAN MEMUNCULKAN PEMBERITAHUAN
        if(txtNIM.getText().equals("")||txtNAMA.getText().equals("")||txtALAMAT.getText().equals("")||txtEMAIL.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Data Di Text Field Kosong, Silahkan Input Data Pada Text Field Terlebih Dahulu...!", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            try
            {
                String QuerySql = "INSERT INTO mahasiswa VALUES ('"+txtNIM.getText()+"','"+txtNAMA.getText()+"','"+cbPRODI.getSelectedItem()+"','"+txtALAMAT.getText()+"','"+txtEMAIL.getText()+"')";
                //KONEKSI KE DATABASE
                java.sql.Connection conn = (Connection)Config.configDB();
                //FUNGSI prepareStatement UNTUK SEBUAH PERNYATAAN QuerySql KE DATABASE
                java.sql.PreparedStatement pstm = conn.prepareStatement(QuerySql);
                //FUNGSI UNTUK MENGEKSEKUSI PERNYATAAN QuerySql KE DATABASE
                pstm.execute(); 
                JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil..", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                menampilkan_TabelMahasiswa();
                kosongkan_form();
            }
            catch(HeadlessException | SQLException e)
            {
                //PESAN ERROR JIKA PENGUJIAN TIDAK SESUAI
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_tbSIMPANActionPerformed

