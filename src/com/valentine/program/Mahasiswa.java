    private void menampilkan_TabelMahasiswa(){
        
       //FUNGSI UNTUK MENAMPILKAN TABEL
       DefaultTableModel table=  new DefaultTableModel();
       table.addColumn("No.");
       table.addColumn("NIM");
       table.addColumn("Nama Mahasiswa");
       table.addColumn("Prodi");
       table.addColumn("Alamat");
       table.addColumn("Email");
       
       try
       {    
            int no = 1;
            //QUERY UNTUK MENAMPILKAN ISI TABLE DI DATABASE
            String QuerySql = "Select * FROM mahasiswa";
            //KONEKSI KE DATABASE
            java.sql.Connection conn = (com.mysql.jdbc.Connection)Config.configDB();
            //PEMBUATAN PERNYATAAN KONEKSI
            java.sql.Statement stm = conn.createStatement();
            //FUNGSI UNTUK MENGEKSEKUSI PERNYATAAN QuerySql KE DATABASE 
            java.sql.ResultSet rs = stm.executeQuery(QuerySql);
            
            //MENAMPILKAN ISI DATABASE KE TABEL MAHASISWA DENGAN MENGGUNAKAN ARRAY SESUAI DENGAN INDEX DI DATABASE DAN FORMAT TABLE MAHASISWA 
            while(rs.next())
            {
            table.addRow(new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            }
            Tabel.setModel(table);
        }
        catch (SQLException e)
        {
            System.out.println("Error....! " + e.getMessage());
        }
    }

